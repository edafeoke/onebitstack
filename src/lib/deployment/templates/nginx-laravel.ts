export function generateLaravelNginxConfig(input: LaravelNginxInput): string {
  const tls = resolveNginxTlsPaths({
    tlsCertPath: input.tlsCertPath,
    tlsKeyPath: input.tlsKeyPath,
    serverName: input.serverName
  });
  const withTls: LaravelNginxInput = {
    ...input,
    tlsCertPath: tls?.cert,
    tlsKeyPath: tls?.key
  };
  const hasTls = Boolean(tls?.cert && tls?.key);

  const blocks = hasTls
    ? [httpRedirectServer(withTls.serverName), "", laravelHttpsServer(withTls)]
    : [laravelHttpOnlyServer(withTls)];

  return joinLines(["# Generated by central-server (laravel)", "", ...blocks]);
}
